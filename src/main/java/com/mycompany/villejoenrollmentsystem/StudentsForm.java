    public void LoadRecord(){
        VillejoEnrollmentSystem a = new VillejoEnrollmentSystem();
        a.DBConnect();
        
        DefaultTableModel tblmodel = (DefaultTableModel) studTable.getModel();
        tblmodel.setRowCount(0);
        
        try{
            String queryShow = "SELECT * from students;";
            a.rs = a.st.executeQuery(queryShow);
            while(a.rs.next()){
                String studid = a.rs.getString("studID");
                String studname = a.rs.getString("studname");
                String studcontact = a.rs.getString("studcontact");
                String studaddress = a.rs.getString("studaddress");
                String studEmail = a.rs.getString("studEmail");
                String studgender = a.rs.getString("studgender");
                String studyrlvl = a.rs.getString("studyrlvl");
                
            tblmodel.addRow(new Object[]{studid, studname, studcontact, studaddress, studEmail, studgender, studyrlvl});
                
            }
        }catch (Exception ex){
            System.out.println(ex);
        }
    }
    
    public void ShowRecord(){
        DefaultTableModel tblmodel = (DefaultTableModel) studTable.getModel();
        tblmodel.setRowCount(0);
        
        VillejoEnrollmentSystem a = new VillejoEnrollmentSystem();
        a.DBConnect();
        
        try{
            String queryShow = "SELECT * from students;";
            a.rs = a.st.executeQuery(queryShow);
            while (a.rs.next()){
                String studid = a.rs.getString("studID");
                String studname = a.rs.getString("studname");
                String studcontact = a.rs.getString("studcontact");
                String studaddress = a.rs.getString("studaddress");
                String studEmail = a.rs.getString("studEmail");
                String studgender = a.rs.getString("studgender");
                String studyrlvl = a.rs.getString("studyrlvl");
                
                String [] studRecord = {studid, studname, studcontact, studaddress, studEmail, studgender, studyrlvl};
                tblmodel.addRow(studRecord);
            }
        } catch (Exception ex){
            System.out.println(ex);
        }
    }
    private void saveMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_saveMouseClicked
        Students a = new Students();
        a.SaveRecord(Integer.parseInt(studID.getText()),
                studname.getText(),
                studcontact.getText(),
                studaddress.getText(),
                studEmail.getText(),
                studgender.getText(),
                studyrlvl.getText());
        
        ShowRecord();
    }//GEN-LAST:event_saveMouseClicked

    private void deleteMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_deleteMouseClicked
        VillejoEnrollmentSystem a = new VillejoEnrollmentSystem();
        a.DBConnect();
        
        int sID = Integer.parseInt(studID.getText());
        
        Students deleteRec = new Students();
        deleteRec.DeleteRecord(sID);
        
        ShowRecord();
    }//GEN-LAST:event_deleteMouseClicked

    private void updateMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_updateMouseClicked
        int sID = Integer.parseInt(studID.getText());
        String sname = studname.getText();
        String con = studcontact.getText();
        String add = studaddress.getText();
        String mail = studEmail.getText();
        String gen = studgender.getText();
        String yrlvl = studyrlvl.getText();
        
        Students updateRec = new Students();
        updateRec.UpdateRecord(sID, sname, con, add, mail, gen, yrlvl);
        
        ShowRecord();
    }//GEN-LAST:event_updateMouseClicked

